package operator;
//operator is a symbol that perform operations on variables and values
//Types of operators --Arithmetic,Unary,Relational,Conditional,Assignment operator

public class Operatorarithmetic {     
	public static void main(String[] args) {        //Arithmetic--  +,-,*,/,%
		double i=30;
		double j=20;
		System.out.println(i+j);
		System.out.println(i-j);
		System.out.println(i*j);
		System.out.println(i/j);       //when we want quotient
		System.out.println(i%j);       //when we want remainder

	}                           
}
